package settings;

public class DataBaseSettings {
	private String username;
	private String password;
	private String address;
	private int port;
	
	public DataBaseSettings() {
		
	}
	
	public String getUsername() {
		return username;
	}


	public String getPassword() {
		return password;
	}


	public String getAddress() {
		return address;
	}


	public int getPort() {
		return port;
	}


	
	
}
